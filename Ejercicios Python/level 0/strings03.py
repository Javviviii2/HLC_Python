### format()
age = 10
txt= "Hola, tengo {} años " # las llaves junto con format, introduce las variables en la llave
print(txt.format(age))

nombre="Javi"
apellido1 = "Lok"
apellido2 = "koi"
saludo= "Hola {} {} {}, ¿cómo estas?" 

print(saludo.format(nombre, apellido1, apellido2)) # el primer hueco corresponde con la 1º var que le mando

saludo2= "Hola {2} {1} {0}, ¿cómo estas?" 
print(saludo2.format(nombre, apellido1, apellido2))

### escape characters \' or \n
